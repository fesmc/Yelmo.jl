#!/usr/bin/env julia
#
# benchmarks/new.jl — scaffold a new benchmark directory.
#
# Usage:
#   julia benchmarks/new.jl <name>
#
# Creates benchmarks/<name>/ with the standard layout (README.md,
# Project.toml, Manifest.toml, run.jl, summary.jl, summary.json,
# .gitignore). The Project.toml is wired to develop the local Yelmo.jl
# package (so the benchmark always runs against the worktree's source)
# and includes NCDatasets + Statistics by default.
#
# Errors out if benchmarks/<name>/ already exists.
#
# After scaffolding, edit run.jl and summary.jl to implement the
# specific benchmark, then add a row to benchmarks/README.md.

using Pkg

const BENCHMARKS_DIR = @__DIR__
const REPO_ROOT      = abspath(joinpath(BENCHMARKS_DIR, ".."))

function usage()
    println(stderr, "Usage: julia benchmarks/new.jl <name>")
    println(stderr)
    println(stderr, "Creates benchmarks/<name>/ with the standard layout.")
    exit(1)
end

