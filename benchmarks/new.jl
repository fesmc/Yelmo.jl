const GITIGNORE_TEMPLATE = """
# The repo's root .gitignore ignores Manifest.toml globally (standard
# Julia package convention). Benchmark projects are end-user projects,
# not packages — we *want* the Manifest committed for reproducibility.
!Manifest.toml

output/
