"""

function template_render(s::AbstractString, name::AbstractString)
    return replace(s, "{{NAME}}" => name)
end

# ----------------------------------------------------------------------
# Project.toml setup.
# ----------------------------------------------------------------------

# Write a minimal Project.toml that pins Yelmo to the local repo via a
# relative `[sources]` entry, then resolve + instantiate. We write the
# file by hand rather than using `Pkg.develop(path=...)` because the
# latter resolves relative paths against the current working directory
# (not the project), which breaks when the scaffolder is invoked from
# the repo root.
const _YELMO_UUID                = "f480e141-c095-4a5e-9e4c-f8685db772bc"
const _ICE_SHEET_BENCHMARKS_UUID = "8c4d0a2e-6f3b-4e0a-9f6e-1e2d3a4b5c6d"
const _ICE_SHEET_BENCHMARKS_URL  = "https://github.com/fesmc/IceSheetBenchmarks.jl.git"
# Pinned per the dep-swap commit; bump when a new ISB release lands.
const _ICE_SHEET_BENCHMARKS_REV  = "ce8c2731da6d142705e442040ee48a14b65177bf"
const _STDLIB_DEPS = (
    "Statistics" => "10745b16-79ce-11e8-11f9-7d13ad32a3b2",
    "Printf"     => "de0858da-6303-5e67-8744-51eddeeeb8d7",
    "Dates"      => "ade2ca70-3891-5945-98fb-dc099432e06a",
    "Pkg"        => "44cfe95a-1eb2-52ea-b672-e2afdf69b78f",
)

function _write_project_toml(target_dir::AbstractString)
    project_path = joinpath(target_dir, "Project.toml")
    open(project_path, "w") do io
        println(io, "[deps]")
        println(io, "Yelmo = \"$(_YELMO_UUID)\"")
        println(io, "IceSheetBenchmarks = \"$(_ICE_SHEET_BENCHMARKS_UUID)\"")
        for (name, uuid) in _STDLIB_DEPS
            println(io, "$name = \"$uuid\"")
        end
        println(io)
        println(io, "[sources]")
        # Yelmo path is relative to the project (benchmark) dir; ISB
        # is pinned to a specific commit on the standalone repo so
        # benchmark Manifests are reproducible.
        println(io, "Yelmo = {path = \"../..\"}")
        println(io, "IceSheetBenchmarks = {url = \"$_ICE_SHEET_BENCHMARKS_URL\", rev = \"$_ICE_SHEET_BENCHMARKS_REV\"}")
    end
    return project_path
end

function _setup_project(target_dir::AbstractString)
    _write_project_toml(target_dir)
    Pkg.activate(target_dir)
    try
        # NCDatasets, JSON, and Oceananigans aren't deps of Yelmo /
        # IceSheetBenchmarks at the benchmark's project level — add
        # them here. Oceananigans is the most common need (for
        # `interior(field)`); keep it default to avoid surprising
        # missing-package errors at first run.
        Pkg.add(["NCDatasets", "JSON", "Oceananigans"])
        Pkg.instantiate()
    finally
        Pkg.activate()
    end
    return nothing
end

# ----------------------------------------------------------------------
# Main.
# ----------------------------------------------------------------------

function main()
    length(ARGS) == 1 || usage()
    name = ARGS[1]
    occursin(r"^[A-Za-z0-9_\-]+$", name) ||
        error("benchmark name must match [A-Za-z0-9_-]+ (got '$name')")
    name in ("new", "README", "_template") &&
        error("'$name' is a reserved name; pick something else.")

    target = joinpath(BENCHMARKS_DIR, name)
    isdir(target) && error("benchmarks/$name/ already exists")
    mkpath(target)

    write(joinpath(target, "README.md"),    template_render(README_TEMPLATE, name))
    write(joinpath(target, "run.jl"),       template_render(RUN_TEMPLATE, name))
    write(joinpath(target, "summary.jl"),   template_render(SUMMARY_TEMPLATE, name))
    write(joinpath(target, ".gitignore"),   GITIGNORE_TEMPLATE)

    @info "scaffolding $name — resolving Project.toml + Manifest.toml…"
    _setup_project(target)

    # Initial summary.json placeholder so the file exists from the start.
    open(joinpath(target, "summary.json"), "w") do io
        write(io, """{
  "benchmark": "$(name)",
  "date": null,
  "yelmo_version": null
}
""")
    end

    println()
    @info "Created benchmarks/$name/" path=target
    println("""
Next steps:
  1. Edit benchmarks/$name/run.jl     — fill in the simulation logic.
  2. Edit benchmarks/$name/summary.jl — emit summary.json statistics.
  3. Edit benchmarks/$name/README.md  — describe the benchmark.
  4. Add a row to benchmarks/README.md.
  5. Run:    julia --project=benchmarks/$name benchmarks/$name/run.jl
             julia --project=benchmarks/$name benchmarks/$name/summary.jl
""")
end

main()
